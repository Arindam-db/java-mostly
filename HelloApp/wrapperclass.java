    public static void main(String[] args){
        //wrapper class = provides a way to use primitive data types as a reference data types.

        //primitive     //Wrapper
        
        //boolean       //Boolean
        //char          //Character
        //int           //Integer
        //double        //Double

        // autoboxing = the automatic conversion that the java compiler makes between the primitive types and their corresponding wrapper class
        // unboxing =  vice versa of autoboxing , automatic conversion of wrapper class to primitive .

        Boolean a = true;
        Character b = '@';
        Integer c = 234;
        Double d = 3.1456;
        String e = "Arindam";
        
    }   
